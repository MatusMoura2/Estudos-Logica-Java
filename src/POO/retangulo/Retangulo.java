package POO.retangulo;

public class Retangulo {

	private float lado01;
	private float lado02;
	private float lado03;
	private float lado04;
	
	public Retangulo(float lado01, float lado02, float lado03, float lado04) {
		this.lado01 = lado01;
		this.lado02 = lado02;
		this.lado03 = lado03;
		this.lado04 = lado04;
	}

	public float getLado01() {
		return lado01;
	}

	public void setLado01(float lado01) {
		this.lado01 = lado01;
	}

	public float getLado02() {
		return lado02;
	}

	public void setLado02(float lado02) {
		this.lado02 = lado02;
	}

	public float getLado03() {
		return lado03;
	}

	public void setLado03(float lado03) {
		this.lado03 = lado03;
	}

	public float getLado04() {
		return lado04;
	}

	public void setLado04(float lado04) {
		this.lado04 = lado04;
	}
	
	
	
	
}
